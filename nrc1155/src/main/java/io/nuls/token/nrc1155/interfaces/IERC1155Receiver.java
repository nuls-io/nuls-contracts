package io.nuls.token.nrc1155.interfaces;

import io.nuls.contract.sdk.Address;

import java.math.BigInteger;

/**
 * @author: PierreLuo
 * @date: 2023/4/23
 */
public interface IERC1155Receiver extends INRC165 {
    /**
     * @dev Handles the receipt of a single ERC1155 token type. This function is
     * called at the end of a `safeTransferFrom` after the balance has been updated.
     *
     * NOTE: To accept the transfer, this must return true
     *
     * @param operator The address which initiated the transfer (i.e. msg.sender)
     * @param from The address which previously owned the token
     * @param id The ID of the token being transferred
     * @param value The amount of tokens being transferred
     * @param data Additional data with no specified format
     * @return `true` if transfer is allowed
     */
    boolean onERC1155Received(
            Address operator,
            Address from,
            BigInteger id,
            BigInteger value,
            String data
    );

    /**
     * @dev Handles the receipt of a multiple ERC1155 token types. This function
     * is called at the end of a `safeBatchTransferFrom` after the balances have
     * been updated.
     *
     * NOTE: To accept the transfer(s), this must return true
     *
     * @param operator The address which initiated the batch transfer (i.e. msg.sender)
     * @param from The address which previously owned the token
     * @param ids An array containing ids of each token being transferred (order and length must match values array)
     * @param values An array containing amounts of each token being transferred (order and length must match ids array)
     * @param data Additional data with no specified format
     * @return `true` if transfer is allowed
     */
    boolean onERC1155BatchReceived(
            Address operator,
            Address from,
            BigInteger[] ids,
            BigInteger[] values,
            String data
    );
}
